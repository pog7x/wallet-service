// Package main is the entry point for the application.
package main

import (
	"fmt"
	"runtime"
	"time"
)

// go build -o app . && ./app
// sysmon шлёт SIGURG →
// обработчик останавливает горутину в безопасной точке →
// остановка мира завершается →
// программа завершается
//
// go build -o app . && GOMAXPROCS=1 GODEBUG=asyncpreemptoff=1 timeout 5 ./app
// цикл без вызовов не имеет кооперативных точек →
// без асинхронного вытеснения он не доходит до безопасной точки →
// остановка мира не завершается →
// runtime.GC() блокируется →
// программа виснет
//
// Требование безопасной точки для остановки мира.
// runtime.GC() запускает
// сборку мусора, а той нужна фаза остановки мира (stop-the-world): все горутины
// должны быть приостановлены в безопасной точке, где среда выполнения точно знает,
// какие значения на стеке и в регистрах являются указателями. Пока хоть одна горутина
// не в безопасной точке, остановка мира не может завершиться.
//
// Почему без вытеснения происходит зависание.
// С asyncpreemptoff=1
// единственный способ прервать горутину — кооперативный, то есть в точке проверки
// роста стека в начале функции. Тесный арифметический цикл без вызовов таких точек
// не содержит, поэтому горутина никогда добровольно не доходит до безопасной точки.
// Остановка мира ждёт её вечно, runtime.GC() в main не возвращается, печати нет.
//
// Почему обычный запуск работает.
// С асинхронным вытеснением поток sysmon
// (system monitor) замечает, что горутина исполняется дольше 10 миллисекунд, и
// посылает её потоку сигнал SIGURG. Обработчик сигнала останавливает горутину в
// сгенерированной компилятором безопасной точке (для этого компилятор строит
// карты указателей для мест, где вытеснение допустимо). Остановка мира завершается,
// сборка мусора проходит, main возобновляется и печатает строку.

func main() {
	go func() {
		i := 0
		for {
			i++
			_ = i
		}
	}()

	time.Sleep(50 * time.Millisecond) // отдаём единственный P циклу
	runtime.GC()                      // остановка мира ждёт безопасной точки в цикле
	fmt.Println("main resumed: preemption worked")
}
